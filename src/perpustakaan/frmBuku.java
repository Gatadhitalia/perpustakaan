    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String tKode=txtKode.getText();
        String tIsbn=txtIsbn.getText();
        String judul=txtJudul.getText();
        String penulis=txtPenulis.getText();
        String penerbit=txtPenerbit.getText();
        try{
            if (edit==true){
                stm.executeUpdate("update buku set isbn='"+tIsbn+"',judul_buku='"
                    +judul+"',penulis='"+penulis+"',penerbit='"+penerbit
                    +"',status='"+sStatus+"' where kd_Buku='"+ tKode + "'");
            }else{
                stm.executeUpdate("INSERT into buku VALUES('"+tKode+"','"+tIsbn
                    +"','"+judul+"','"+penulis+"','"+penerbit+"','"+sStatus+"')");
            }
            tblBuku.setModel(new DefaultTableModel(dataTable,header));
            baca_data();
            aktif(false);
            setTombol(true);
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Data belum dipilih");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

